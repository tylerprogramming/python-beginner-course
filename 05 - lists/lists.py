# Lists
fruits = ["apple", "banana", "cherry"]

# Accessing elements
print("First fruit:", fruits[0])  # Output: apple

# Modifying elements
fruits[1] = "blueberry"
print("Modified list:", fruits)   # Output: ['apple', 'blueberry', 'cherry']

# Adding elements
fruits.append("date")
print("Appended list:", fruits)   # Output: ['apple', 'blueberry', 'cherry', 'date']

# Removing elements
fruits.remove("apple")
print("List after removal:", fruits) # Output: ['blueberry', 'cherry', 'date']